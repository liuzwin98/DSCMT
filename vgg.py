def init_from_pretrain(model, pretrain, strict=False):
    pretrain_rgb = pretrain.copy()
    pretrain_dep = pretrain_rgb.copy()
    for k, v in list(pretrain_dep.items()):
        prefix = k.split('.')[0]
        if 'features' == prefix:
            pretrain_dep.update({k.replace('features', 'features2'): pretrain_dep.pop(k)})
        elif 'classifier' == prefix:
            pretrain_dep.update({k.replace('classifier', 'classifier2'): pretrain_dep.pop(k)})

    pretrain_new = pretrain_rgb.copy()
    pretrain_new.popitem()      # 去除最后一层fc的参数
    pretrain_new.popitem()
    pretrain_dep.popitem()
    pretrain_dep.popitem()
    # del pretrain_new['classifier.0.weight']
    # del pretrain_new['classifier.0.bias']
    # del pretrain_dep['classifier2.0.weight']
    # del pretrain_dep['classifier2.0.bias']
    pretrain_new.update(pretrain_dep)       # 将另一路添加进单路模型

    model.load_state_dict({k: v for k, v in pretrain_new.items()}, strict=strict)